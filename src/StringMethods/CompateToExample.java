package StringMethods;

public class CompateToExample {

	public static void main(String[] args) {
		
		String s1="Hello";
		String s2="Hello";
		String s3="Helmet";
		String s4="Welcome";
		
		System.out.println(s1.compareTo(s2)); 
		System.out.println(s2.compareTo(s3));
		System.out.println(s4.compareTo(s3));		
	}
}
